
package modelo;

public class Posicion {
    private int idposicion;
    private String nom_posicion;
    private String desc_posicion;

    public int getIdposicion() {
        return idposicion;
    }

    public void setIdposicion(int idposicion) {
        this.idposicion = idposicion;
    }

    public String getNom_posicion() {
        return nom_posicion;
    }

    public void setNom_posicion(String nom_posicion) {
        this.nom_posicion = nom_posicion;
    }

    public String getDesc_posicion() {
        return desc_posicion;
    }

    public void setDesc_posicion(String desc_posicion) {
        this.desc_posicion = desc_posicion;
    } 
}
